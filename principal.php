
<h4>
	CHAMADA PARA SUBMISSÃO DE RESUMOS EXPANDIDOS<br />
	IX JORNADA CIENTÍFICA DO IFMT CAMPUS SÃO VICENTE
</h4>
<p>
1. A IX Jornada Científica do IFMT Campus São Vicente terá como uma das ações, a apresentação de Trabalhos Científicos, na forma de “Resumos Expandidos”;<br />
2. Os Resumos Expandidos devem ter de 3 a 4 páginas e o(a) autor(a) deverá no ato da submissão escolher uma das 4 formas de apresentação:<br />
<ol type="a">
	<li>Pôster para apresentação na sede do Campus São Vicente;</li>
	<li>Experimento para apresentação na Feira do Conhecimento a ser realizada no Centro de Referência de Jaciara.</li>
	<li>Vídeo para apresentação na Feira do Conhecimento a ser realizada no Centro de Referência de Jaciara.</li>
	<li>Painel para apresentação na Feira do Conhecimento a ser realizada no Centro de Referência de Jaciara.</li>
</ol>
3. Os resumos, para serem apresentados, deverão ser submetidos ao Comitê Científico do Evento, conforme cronograma apresentado a seguir: 
<table class="table table-striped">
	<thead>
		<th>Atividade</th>
		<th>Data</th>
	</thead>
	<tr>
		<td>Lançamento e divulgação das normas para submissão</td>
		<td>15 de agosto</td>
	</tr>
	<tr>
		<td>Abertura do sistema para submissão (submissão “on line”)</td>
		<td>17 de agosto</td>
	</tr>
	<tr>
		<td>Prazo para submissão dos Trabalhos</td>
		<td>Até 06 de setembro</td>
	</tr>
	<tr>
		<td>Divulgação do Resultado </td>
		<td>Até 20 de setembro</td>
	</tr>
	<tr>
		<td>Apresentação dos trabalhos</td>
		<td>29 de setembro</td>
	</tr>
</table>
4. O Comitê Científico, após avaliação do resumo expandido, emitirá um dos seguintes pareceres: 
<ol type="a">
	<li>Aceito</li>
    <li>Aceito para apresentação, mas deve ser corrigido para publicação</li> 
    <li>Não Aceito</li>
</ol>
5. Os trabalhos enquadrados no item “b” poderão ser apresentados no Evento na forma de pôster, mas deverão ser corrigidos conforme parecer do Comitê Científico para publicação nos Anais.<br />
6. Os trabalhos enquadrados no item “c” não poderão ser apresentados e nem publicados nos Anais do Evento.<br />
7. Os trabalhos serão submetidos ao Comitê Científico na forma de RESUMO EXPANDIDO. A avaliação e seleção dos trabalhos será realizada pelo Comitê Científico do Evento<br />
8. SUBMISSÃO: Os Resumos Expandidos deverão ser submetidos pelo primeiro autor por meio do formulário “on line” disponível para acesso no link <a href="http://www.ifmtsvc.edu.br/jornadacientifica/jornada2018/">http://www.ifmtsvc.edu.br/jornadacientifica/jornada2018/</a>  O sistema para submissão será aberto no dia 17/08/2018 e fechado às 23h59min do dia 06/09/2018 (horário de MT), conforme cronograma apresentado no item 3 deste regulamento. <br />
9.  No formulário “on line”, o proponente fará a sua inscrição/cadastro e criará uma senha para acesso. Em seguida, fará a submissão do Resumo Expandido com extensão PDF.   <br />
10.  Poderão ser submetidos e selecionados trabalhos de Ensino, Pesquisa e Extensão e Inovação nas diversas áreas do conhecimento, segundo a classificação do CNPq. <br />
11. Todos os trabalhos aceitos serão publicados nos Anais do Evento. <br />
12.  Certificação: Será emitido certificado de apresentação de Resumo Expandido ao autor e até o limite de 4 (quatro) co-autores por trabalho. <br />
13.  A certificação só será emitida se o trabalho for apresentado no evento. Pelo menos um dos autores deverá estar presente na data e hora programada para apresentação. <br />
14.  O Comitê Científico recomenda que sejam submetidos apenas trabalhos que já apresentem resultados finais; <br />
15.  A submissão de Resumo Expandido é aberta a todos, ou seja, além dos trabalhos desenvolvidos no IFMT, o evento receberá também trabalhos externos.<br />
16.  O Comitê Científico certificará os melhores trabalhos apresentados no evento, podendo ser em uma única categoria (geral) ou em divididos em categorias específicas.<br />
</p>

<h4>Divulgado o modelo do RESUMO EXPANDIDO para a IX Jornada Científica</h4>

<p>
A versão completa dos resumos expandidos será publicada nos Anais do IX Jornada Científica do IFMT Campus São Vicente. Portanto, é extremamente importante que o preparo da versão digital do seu trabalho esteja de acordo com as presentes instruções.<br />O Comitê Científico da IX Jornada Científica do IFMT Campus São Vicente terá a sua disposição a cópia eletrônica de cada trabalho no sistema do evento, para a sua correspondente avaliação por especialistas. O trabalho aceito poderá estar sujeito a revisões por parte de seus autores, para a sua inclusão nos Anais.
</p>
